package com.eventivities.android.domain;


public class PuntuacionLocal {
	private int idPuntuacionLocal=0;
	private int puntuacion=0;
	private int idlocal=0;
	private int idUsuario=0;
	
	public int getIdPuntuacionLocal() {
		return idPuntuacionLocal;
	}
	public void setIdPuntuacionLocal(int idPuntuacionLocal) {
		this.idPuntuacionLocal = idPuntuacionLocal;
	}
	public int getPuntuacion() {
		return puntuacion;
	}
	public void setPuntuacion(int puntuacion) {
		this.puntuacion = puntuacion;
	}
	public int getIdlocal() {
		return idlocal;
	}
	public void setIdlocal(int idlocal) {
		this.idlocal = idlocal;
	}
	public int getIdUsuario() {
		return idUsuario;
	}
	public void setIdUsuario(int idUsuario) {
		this.idUsuario = idUsuario;
	}
			
}
